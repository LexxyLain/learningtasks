    <body>
    <h2> Ingredients </h2>
    <li>1 fillet of Salmon, skinned (tails are the cheapest).</li>
    <li>Any veggies you like: tomatoes, pepper, zucchinis, etc.</li>
    <li>Liquid Cream</li>
    <li>Black Pepper</li>
    <li>Salt</li>

    <h2>Methods </h2>
    <p>1. Put the salmon in an oven dish - add chopped veggies and cream on top with salt and pepper.</p>
    <p>2. Grill on a high heat for about 25 minutes. If it is a thick fillet, it will take longer to cook through, always check the middle if cooked.</p>
    <p>3. You can serve it with rice or pasta.</p>
