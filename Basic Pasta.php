        <h2>Ingredients</h2>
        <li>Dried Pasta (any pasta will do)</li>
        <li>Water</li>
        <li>Salt</li>
        <li>Tomato Puree</li>
        <li>Cheese/Tuna/Both/None</li>

        <h2>Method</h2>
        <p>1. Boil your pasta adding salt to the water for later on.</p>
        <p>2. While pasta is cooking grate your cheese and drain your tuna.</p>
        <p>3. When the oasta is cook add about half a tube of tomato puree, stirring well, to give you a pasta consistency.
            Of course, you can use passata if you want, and drain the pasta instead.</p>
        <p>4. Then, add the tuna if using any, stirring it in well. Leave for a minute to heat up the tuna. ADdd salt/pepper to taste.</p>
        <p>5. Serve into a bowl and sprinkle lots of cheese on top.</p>
