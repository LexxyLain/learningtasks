        <h2>Ingredients</h2>

        <li>2 Chicken Breasts</li>
        <li>1 can of Coconut Milk, or Low Fat Coconut Milk</li>
        <li>1 Onion</li>
        <li>1 Aubergine</li>
        <li>1 chopped Green Chili</li>
        <li>Thai Green Curry Paste</li>

        <h2>Method</h2>

        <p>1. To make the curry, cook the paste in a large non-stick wok or large frying pan over a low heat, for 5 minutes. 
           Cut the chicken into strips and add to the pan with the chopped onion. Cook for 5-8 minutes
           or until no longer pink. Add the aubergine chopped.</p>
        <p>2. Stir in the coconut milk and simmer for about 10 minutes or until the chicken is cooked through. Scatter over the chili and serve with rice.</p>
