    <h2>Ingredients</h2>
    <li>Rice (as much as you fancy really!)</li>
    <li>Half an onion (or more, again if you fancy it)</li>
    <li>A tin of sweetcorn</li>
    <li>A few mushrooms</li>
    <li>1-2 Eggs</li>

    <h2> Methods </h2>
    <p>1. Start cooking the rice however you do, and put some oil in a frying pan.</p>
    <p>2. Chop the onion into bits, any size really, doesnt matter particularly and throw into the frying pan.</p>
    <p>3. Chop the mushrooms and add the on</p>
    <p>4. Once the rice is cooked, drain it off and add it into the frying pan.</p>
    <p>5. Tip the drained sweetcorn into the pan as well and stir it all up.</p>
    <p>6. Break the eggs into the pan and stir the mixture up.</p>
    <p>7. At this point, you need to keep stirring the mixture, keeping the eggs moving. Keep going until the eggs have cooked. </p>
