        <h2>Ingredients</h2>
        <li>3 Eggs</li>
        <li>Salt and Pepper</li>
        <li>Butter</li>
        <li>Grated Cheese (optional)</li>
        <li>Mixed Herbs (optional)</li>

        <h2>Method</h2>
        <p>1. Mix the eggs in any sort of bowl or jug you like (a pint glass works well) with salt, pepper and herbs if desired. </p>
        <p>2. Heat the butter (or oil) in a reasonably large frying pan.</p>
        <p>3. Pour in the egg mixture slowly and swirl the pan a little to even it out.</p>
        <p>4. After 30 seconds or so the top should firm up, at this point add grated cheese if you like.</p>
        <p>5. Continue cooking a little until the cheese begins to melt, then fold the omelette in half with a bendy spatula.</p>
        <p>6. If it's nice and cooked on the bottom already then serve, otherwise, continue cooking on each side a little more.</p>
