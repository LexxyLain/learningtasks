    <h2>Ingredients</h2>

    <li>Ground/minced beef 500g</li>
    <li>1 Large onion chopped</li>
    <li>2-3 Cloves of Garlic</li>
    <li>1-2 Tins of chopped tomatoes</li>
    <li>Squeeze tomato puree</li>
    <li>1 teaspoon of chili powder (or to taste)</li>
    <li>1 teaspoon of Ground cumin</li>
    <li>Dash of Worcester Sauce</li>
    <li>Sprinkle of Salt and</li>
    <li>1 chopped Red Pepper</li>
    <li>1 tin of drained Kidney Beans</li>

    <h2>Methods</h2>
    
    <p>1. Fry the onion in a hot pan with oil until nearly brown then add chopped garlic.</p>
    <p>2. Add the mince and stir until brown - drain any excess fat if desired.</p>
    <p>3. Add all the dried spices and seasoning then reduce heat and add chopped tomatoes.</p>
    <p>4. Stir well and add tomato puree and Worcester sauce then leave to simmer for about an hour (less if you are in a rush).</p>
    <p>5. Add the chopped red pepper and continue to simmer for 5 mins, then add the tin of drained kidney beans and cook for a 
        further 5 mins. If the chili become to dry at any point just add a bit of water. Serve with rice jacket potatoes, or pasta.</p>
    
